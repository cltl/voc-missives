package utils;

public class AbnormalProcessException extends Exception {
    public AbnormalProcessException() {
        super();
    }

    public AbnormalProcessException(String message, Throwable cause) {
        super(message, cause);
    }

    public AbnormalProcessException(String message) {
        super(message);
    }

    public AbnormalProcessException(Throwable cause) {
        super(cause);
    }



}
